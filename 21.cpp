#include<iostream>
using namespace std;

int main()
{
    cout<<"-----------Program to display the size of different datatype-----------";
    cout<<"\nSize of Int :- "<<sizeof(int)<<" bytes";
    cout<<"\nSize of Float :- "<<sizeof(float)<<" bytes";
    cout<<"\nSize of Double :- "<<sizeof(double)<<" bytes";
    cout<<"\nSize of Char :- "<<sizeof(char)<<" bytes";
    return 0;
}